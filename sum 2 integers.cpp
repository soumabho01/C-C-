#include <stdio.h>
int main() {
    int num1, num2, sum;
    // Taking input from the user
    printf("Enter the first number: ");
    scanf("%d", &num1);
printf("Enter the second number: ");
    scanf("%d", &num2);
    // Calculating the sum
    sum = num1 + num2;
    // Outputting the result
    printf("Sum: %d + %d = %d\n", num1, num2, sum);
    return 0;
}

